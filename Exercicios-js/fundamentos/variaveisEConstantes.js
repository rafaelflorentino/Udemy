// Palavras Reservadas var, let, const
// Javascrip e uma linguagem fracamente tipada

var a = 3; // declarando uma varivel com var
let b = 4; // declarando uma varivel com let

var a = 30; // var pode redeclarar uma variavel
b = 40; // let não permiter redeclarar uma variavel

console.log(a, b);

a = 300;
b = 600;

console.log(a, b);

const c = 5; // constante
// c = 10; não se pode atribuir um valor a uma constante
console.log(c);
  